		  <p>
 CFWP is a digital and public humanities project focusing on the community film movement of the 1960s and 1970s.  CFWP has two key features. The first is  an online site highlighting workshops with related interviews and archival material.   The second is a dissertation for a PhD in American Studies at Yale University. The former focuses on the history of the movement making available academic scholarship, interviews, and archives online.  Participation is also key and I invite all who have either been a part of the movement or have an interest to join the project. The project is made possible thanks to the generosity of the filmmaking and academic communities to whom I am incredibly grateful.
			<br /><br />


The project centers around the following scholarly argument.   In the 1960s and 1970s,  there was a "community film movement" that served as a key tool of the freedom struggles.  The movement began  as a means of getting the tools of cultural production in the hands of local peoples. Initially begun primarily in urban settings, the model focused on film workshops that set out to train youth in filmmaking. The aims were three fold. First, the organizations offered a space for creative expression. Second, the filmmaking process and distribution of films to local, regional, national and international audiences offered a way  for examining unexplored perspectives. Third,  the training workshops provided vocational training.  At the heart of the community film movement was the goal of  cultural and political empowerment while decentering the dominant images of network television and mass media, which either failed to portray those in the freedom struggle or, when they did, often showed peoples as simply the subject of or perpetrator of violence. 
